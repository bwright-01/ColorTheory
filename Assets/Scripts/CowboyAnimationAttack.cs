using UnityEngine;

public class CowboyAnimationAttack : StateMachineBehaviour {
    [SerializeField] Enemy.EnemyAttack enemyAttack;

    public void AttackImmediately() {
        if (enemyAttack != null) enemyAttack.AttackImmediately();
    }

    // OnStateEnter is called when a transition starts and the state machine starts to evaluate this state
    override public void OnStateEnter(Animator animator, AnimatorStateInfo stateInfo, int layerIndex) {
        if (enemyAttack == null) enemyAttack = animator.GetComponentInParent<Enemy.EnemyAttack>();
    }

